import inspect
from datetime import datetime
import pytest
import logging

import pandas as pd

from aimobile.scraper.appstore.http.appdata import AppStoreSearchRequest


# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.http
@pytest.mark.search
class TestAppStoreSearchRequest:  # pragma: no cover
    # ============================================================================================ #
    def test_search(self, container, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        search = AppStoreSearchRequest(term="health", limit=10, max_pages=5)
        for result in search:
            assert isinstance(result, pd.DataFrame)
            logger.debug(f"\nResult\n{result}")

        assert search.pages == 5
        assert search.apps == 50

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)
