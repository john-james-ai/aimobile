import inspect
from datetime import datetime
import pytest
import logging

from aimobile.data.appstore.http.rating import AppStoreRatingRequest

ID = 544007664

# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.http
@pytest.mark.rating
class TestAppStoreRatingRequest:  # pragma: no cover
    # ============================================================================================ #
    def test_get_ratings(self, container, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        session_handler = container.session.handler()
        scraper = AppStoreRatingRequest(handler=session_handler)
        scraper.get_ratings(app_id=ID)
        assert isinstance(scraper.result, dict)
        logger.debug(scraper.result)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)
