import inspect
from datetime import datetime
import pytest
import logging

from aimobile.service.appstore.controller import AppStoreAppController, AppStoreReviewController


# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.service
@pytest.mark.appstore
@pytest.mark.controller
class TestAppStoreAppController:  # pragma: no cover
    # ============================================================================================ #
    def test_appdata(self, container, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        controller = AppStoreAppController(max_pages=2, verbose=1, max_results_per_page=5)
        controller.scrape(terms=["health"])
        repo = container.appstore.appdata_repo()
        df = repo.getall()
        assert len(df) != 0
        logging.debug(df)
        repo.dedup()
        assert len(df) == 10

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_reviews(self, container, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        # Delete all reviews
        repo = container.appstore.review_repo()
        repo.delete()
        # Process
        controller = AppStoreReviewController(max_pages=2, verbose=1, max_results_per_page=5)
        controller.scrape(category_ids=[6013])
        repo = container.appstore.review_repo()
        df = repo.getall()
        assert len(df) != 0
        logging.debug(df)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\n\tCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)
